-- DDL(데이터 정의어)
-- 테이블(DB, ArrayList) 생성
CREATE TABLE MEMBER(
--  필드(칼럼)명 데이터타입 제약조건
	ID VARCHAR(15) PRIMARY KEY,
	PW VARCHAR(20) NOT NULL,
	NAME VARCHAR(10) NOT NULL
);

-- 테이블 삭제
DROP TABLE MEMBER;

-- DML(데이터 조작어)
-- 		데이터 한 개 한 개와 관련된 처리를 수행
-- C
INSERT INTO MEMBER VALUES('admin', '1234', '관리자');
